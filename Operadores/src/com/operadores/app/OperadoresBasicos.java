package com.operadores.app;

public class OperadoresBasicos {

	public static void main(String[] args) {
			
		int primeiro = 10;
		int segundo = 2;
		
		int soma = primeiro+segundo;
		int subtrair = primeiro - segundo;
		int multiplicacao = primeiro * segundo;
		int divisao = primeiro / segundo;
		float modulo = primeiro % segundo; 
		
		System.out.println("soma: \n primeiro + segundo = " + soma + "\n\n");
		System.out.println(subtrair);
		System.out.println(multiplicacao);
		System.out.println(divisao);
		System.out.println(modulo);
	}

}
